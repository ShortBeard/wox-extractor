use std::collections::HashMap;

#[derive(Debug)]
pub enum GameType {
    CLOUDS,
    DARKSIDE,
    SWORDS, //swords currently unsupported
    INTRO,
    UNKNOWN,
}

pub fn get_file_names(game_type: GameType) -> HashMap<u32, String> {
    println!("Game type is: {:?} ", game_type);
    match game_type {
        GameType::CLOUDS => get_file_names_map_clouds(),
        GameType::DARKSIDE => get_file_names_map_darkside(),
        GameType::SWORDS => get_file_names_map_swords(), //swords currently unsupported
        GameType::INTRO => get_file_names_map_intro(),
        _ => {
            println!("Error while trying to match file name.");
            panic!()
        }
    }
}

